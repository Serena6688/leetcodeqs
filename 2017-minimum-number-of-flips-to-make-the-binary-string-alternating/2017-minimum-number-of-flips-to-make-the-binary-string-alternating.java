class Solution {
    public int minFlips(String s) {

        int n = s.length();
        // 原字符串长度

        String doubled = s + s;
        // 关键技巧：把字符串拼接两遍
        // 这样长度为 n 的所有子串，就对应了所有“轮转结果”

        int diff1 = 0;
        // 当前窗口 和 模式 "010101..." 的不匹配个数

        int diff2 = 0;
        // 当前窗口 和 模式 "101010..." 的不匹配个数

        int ans = Integer.MAX_VALUE;
        // 记录最小翻转次数（最终答案）


        // i 是窗口的右端点，在 doubled 上滑动
        for (int i = 0; i < doubled.length(); i++) {

            char c = doubled.charAt(i);
            // 当前进入窗口的新字符


            // ===============================
            // 1️⃣ 计算当前下标 i 在两种模板下应该是什么
            // ===============================

            char p1 = (i % 2 == 0) ? '0' : '1';
            // 模式1：010101...
            // 偶数位是 0，奇数位是 1

            char p2 = (i % 2 == 0) ? '1' : '0';
            // 模式2：101010...
            // 偶数位是 1，奇数位是 0


            // ===============================
            // 2️⃣ 把新加入窗口的字符贡献加进去
            // ===============================

            if (c != p1) diff1++;
            // 如果当前字符和 pattern1 不一样 → 需要翻转 → diff1++

            if (c != p2) diff2++;
            // 同理，对 pattern2


            // ===============================
            // 3️⃣ 如果窗口超过大小 n，需要“移出左边元素”
            // ===============================

            if (i >= n) {
                // 当 i >= n，说明窗口长度已经超过 n
                // 现在窗口应该是 [i-n+1 ... i]
                // 所以位置 i-n 要被移出

                char left = doubled.charAt(i - n);
                // 被移出窗口的字符


                // ⚠️ 注意：pattern 是按“全局 index”算的
                // 所以必须用 (i - n) % 2，而不是窗口内 index

                char leftP1 = ((i - n) % 2 == 0) ? '0' : '1';
                // 这个被移出字符，在 pattern1 下“应该是什么”

                char leftP2 = ((i - n) % 2 == 0) ? '1' : '0';
                // 在 pattern2 下“应该是什么”


                // 如果这个字符原来算进了 diff，现在它走了 → 要减掉

                if (left != leftP1) diff1--;
                // 如果它之前是“不匹配”，那 diff1 里多算了它 → 减1

                if (left != leftP2) diff2--;
                // 同理 diff2
            }


            // ===============================
            // 4️⃣ 当窗口大小 == n 时，更新答案
            // ===============================

            if (i >= n - 1) {
                // i >= n-1 时，窗口长度已经达到 n

                // 当前窗口可以看作一种“轮转结果”
                // diff1 / diff2 表示变成交替串的翻转次数

                ans = Math.min(ans, Math.min(diff1, diff2));
                // 取两个模式中较小的
            }
        }

        return ans;
        // 返回最小翻转次数
    }
}